% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
tic;
set(handles.toc,'String','');
cla(handles.axes1);

step = str2num(get(handles.edit1,'String'));
r = str2num(get(handles.edit3,'String'));
sigma = str2num(get(handles.edit4,'String'));
So = str2num(get(handles.edit5,'String'));
K = str2num(get(handles.edit6,'String'));
T = str2num(get(handles.edit2,'String'));
stat = get(handles.popupmenu4,'Value');
rad_butt = get(get(handles.uibuttongroup1,'SelectedObject'), 'Tag');

if stat == 1
    set(handles.popupmenu4,'Value',2)
end

if strcmp(rad_butt,'radiobutton1') == 1
    rad_val = 1;
elseif strcmp(rad_butt,'radiobutton2') == 1
    rad_val = 2;
else
    rad_val = 3;
end

%Changing Maturity date's unit
if  stat == 3
    T = T/12;  
elseif stat == 4
    T = T/365;
end

%---------------------------EUROPEAN OPTION PRICING---------------------------
set(handles.edit9,'String',Erp_Call(step,So,K,r,T,sigma));
set(handles.edit10,'String',Erp_Put(step,So,K,r,T,sigma));

%---------------------------AMERICAN OPTION PRICING---------------------------

%B-S Equation
[Ecall, Eput] = blsprice(So,K,r,T,sigma);
set(handles.edit16,'String',Ecall);
set(handles.edit19,'String',Eput);

%Show Option Price
set(handles.edit11,'String',Amr_Call(step,So,K,r,T,sigma));
set(handles.edit12,'String',Amr_Put(step,So,K,r,T,sigma));

counter = 1:1:So;
EC = zeros([1 So]);
EP = zeros([1 So]);
AC = zeros([1 So]);
AP = zeros([1 So]);

for i = 1:1:So
    EC(i) = Erp_Call(step,i,K,r,T,sigma);
    EP(i) = Erp_Put(step,i,K,r,T,sigma);
    AC(i) = Amr_Call(step,i,K,r,T,sigma);
    AP(i) = Amr_Put(step,i,K,r,T,sigma);
end

curveCall = animatedline('Color','r','linewidth',1.15);
curvePut = animatedline('Color','b','linewidth',1.15);
grid on
hold all

if rad_val == 1
    title('European Call >< European Put','Color','w');
    legend('European Call','European Put');
    for i = 1:1:So
        addpoints(curveCall,counter(i),EC(i));
        addpoints(curvePut,counter(i),EP(i));
        drawnow; pause(0.03);
    end
elseif rad_val == 2
    title('American Call >< American Put','Color','w');
    legend('American Call','American Put');
    for i = 1:1:So
        addpoints(curveCall,counter(i),AC(i));
        addpoints(curvePut,counter(i),AP(i));
        drawnow; pause(0.03);
    end
else
    title('European Put >< American Put','Color','w');
    legend('European Put','American Put');
    for i = 1:1:So
        addpoints(curveCall,counter(i),EP(i));
        addpoints(curvePut,counter(i),AP(i));
        drawnow; pause(0.05);
    end
end
toc;
set(handles.toc,'String',round(toc*100000)/100000);

